// Convert every Gogh terminal theme (Gogh-master/themes/*.yml) into a SLOTH
// theme and write them to lib/gogh-themes.js as a built-in set. SLOTH's theme
// roles take hex colours (truecolor), so each Gogh palette maps directly.
//
// Run from the sloth directory:  node scripts/build-gogh-themes.mjs

import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const here = path.dirname(fileURLToPath(import.meta.url))
const appRoot = path.resolve(here, '..')
const goghThemes = path.join(appRoot, 'Gogh-master', 'themes')
const outFile = path.join(appRoot, 'lib', 'gogh-themes.js')

// Pull color_NN / name / variant out of a Gogh .yml without a YAML dependency.
// Gogh stores values single-quoted with a trailing comment, e.g.
//   color_07: '#B5E4F4'    # Cyan (Prompt)
// so take the quoted contents verbatim (the '#' inside is part of the hex);
// only an unquoted value treats '#' as the start of a comment.
function parseGogh (text) {
  const o = {}
  text.split('\n').forEach((line) => {
    const key = /^\s*([a-z0-9_]+):\s*(.*)$/i.exec(line)
    if (!key) return
    let val = key[2]
    const quoted = /^'([^']*)'/.exec(val) || /^"([^"]*)"/.exec(val)
    if (quoted) {
      val = quoted[1]
    } else {
      val = val.replace(/#.*$/, '').trim() // unquoted: strip trailing comment
    }
    o[key[1].toLowerCase()] = val.trim()
  })
  return o
}

// Map a Gogh palette to SLOTH roles, using Gogh's own colour meanings:
//   color_03 green (command)   -> code
//   color_04 yellow            -> accent
//   color_05 blue (path)       -> link
//   color_06 magenta (var)     -> quote
//   color_07 cyan (prompt)     -> heading
//   color_13 bright blue       -> big
//   color_09 bright black/grey -> dim
// bar stays inverse. Light variants keep the same hues; they read fine.
function toSlothTheme (name, g) {
  function c (key, fallback) {
    var v = g[key]
    return (v && /^#?[0-9a-fA-F]{6}$/.test(v.replace('#', '')) && v.indexOf('#') === 0) ? v : fallback
  }
  return {
    heading: c('color_07', '#00cccc'),
    accent: c('color_04', '#cccc00'),
    link: c('color_05', '#3366cc'),
    code: c('color_03', '#33aa33'),
    quote: c('color_06', '#cc33cc'),
    bar: 'inverse',
    dim: c('color_09', '#888888'),
    big: c('color_13', g.color_05 || '#3366cc')
  }
}

// A stable, lowercase, kebab-ish key from a theme name.
function keyOf (name) {
  return name.trim().toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-+|-+$/g, '')
}

const files = fs.readdirSync(goghThemes).filter((f) => /\.ya?ml$/i.test(f))
const themes = {}
let count = 0
for (const f of files) {
  const text = fs.readFileSync(path.join(goghThemes, f), 'utf-8')
  const g = parseGogh(text)
  const name = g.name || path.basename(f, path.extname(f))
  const key = keyOf(name)
  if (!key || themes[key]) continue // skip dupes/empty
  const t = toSlothTheme(name, g)
  t.name = key
  t.label = name
  themes[key] = t
  count++
}

const header = '// Generated by scripts/build-gogh-themes.mjs from Gogh-master/themes.\n' +
  '// ' + count + ' terminal themes converted to SLOTH role->hex maps. Do not edit by\n' +
  '// hand; re-run the script to regenerate.\n\n' +
  'module.exports = '
fs.writeFileSync(outFile, header + JSON.stringify(themes, null, 2) + '\n')
console.log('wrote', count, 'themes to', path.relative(appRoot, outFile))
